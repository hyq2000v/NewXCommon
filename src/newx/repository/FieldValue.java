package newx.repository;

public class FieldValue {
	
	private Object value = null;
	private int type = 0;

	public FieldValue(){
	}
	
	public FieldValue(Object value, int type) {
		this.value = value;
		this.type = type;
	}
	
	public Object getValue() {
		return value;
	}
	
	public void setValue(Object value) {
		this.value = value;
	}

	public int getType() {
		return type;
	}

	public void setType(int type) {
		this.type = type;
	}
	
	public String toString() {
		if (value == null) {
			return "";
		} else {
			return "{value = " + value.toString() + ", type = " + type + "}";
		}
	}
}
